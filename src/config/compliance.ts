// ── Risk tiering (EU AI Act Art. 6 + Annex III; Art. 5 prohibited practices) ────
export type RiskTier = 'minimal' | 'limited' | 'high_risk' | 'prohibited';

// Annex III high-risk domains — keyword heuristics that SUGGEST a high-risk classification
// (employment screening, credit scoring, etc.). A suggestion only: a human still confirms the
// definitive `masterAssistants.riskClassification`. Previously inline in manage-risk-assessment.ts.
export const HIGH_RISK_KEYWORDS = [
    'Lead Screener', 'HR Assistant', 'Recruitment', 'Recruiter', 'Hiring',
    'Credit Scoring', 'Creditworthiness', 'Loan', 'Insurance Pricing',
    'Biometric', 'Emotion', 'Background Check', 'Worker Management',
] as const;

// Art. 5 — practices that are outright PROHIBITED in the EU. Matching one of these blocks
// deployment entirely (no assessment can clear it).
export const PROHIBITED_KEYWORDS = [
    'Social Scoring', 'Social Credit', 'Predictive Policing',
    'Emotion Recognition Workplace', 'Subliminal Manipulation', 'Untargeted Scraping',
] as const;

const includesAny = (haystack: string, needles: readonly string[]): boolean => {
    const h = haystack.toLowerCase();
    return needles.some(n => h.includes(n.toLowerCase()));
};

/**
 * Heuristic first-pass risk tier from an assistant's name + category. Returns the SUGGESTED
 * tier; the authoritative value is the human-confirmed masterAssistants.riskClassification.
 */
export function classifyRiskByKeywords(name: string, category: string): RiskTier {
    const text = `${name} ${category}`;
    if (includesAny(text, PROHIBITED_KEYWORDS)) return 'prohibited';
    if (includesAny(text, HIGH_RISK_KEYWORDS)) return 'high_risk';
    return 'limited';
}

/** Convenience boolean used by the assessment UI to nudge an admin toward an assessment. */
export function suggestsHighRisk(name: string, category: string): boolean {
    const tier = classifyRiskByKeywords(name, category);
    return tier === 'high_risk' || tier === 'prohibited';
}

// ── General-purpose AI systemic-risk compute trigger (EU AI Act Art. 51) ────────
// A GPAI model trained above this many floating-point operations is presumed to carry
