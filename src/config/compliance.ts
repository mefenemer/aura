// ── EU AI Act jurisdiction ────────────────────────────────────────────────────
// The 27 EU member states (ISO-3166 alpha-2; Greece as 'GR' to match Stripe/Netlify country
// codes). EU-jurisdiction workspaces get Art. 50 disclosure defaults turned on. This list was
// previously copy-pasted into register.ts, onboarding.ts, workspace-ai-disclosure.ts and
// provision-assistant-async.ts — they now all import EU_MEMBER_STATE_SET / isEuCountry.
export const EU_MEMBER_STATES = [
    'AT', 'BE', 'BG', 'CY', 'CZ', 'DE', 'DK', 'EE', 'ES', 'FI', 'FR', 'GR', 'HR', 'HU',
    'IE', 'IT', 'LT', 'LU', 'LV', 'MT', 'NL', 'PL', 'PT', 'RO', 'SE', 'SI', 'SK',
] as const;

export const EU_MEMBER_STATE_SET: ReadonlySet<string> = new Set(EU_MEMBER_STATES);

/** True if the (case-insensitive) ISO country code is an EU member state. */
export function isEuCountry(code: string | null | undefined): boolean {
    return !!code && EU_MEMBER_STATE_SET.has(code.trim().toUpperCase());
}

