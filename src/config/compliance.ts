// ── Public-interest text gate (AC1.3) ───────────────────────────────────────────
// Text touching these topics must carry a visible AI-disclosure label OR pass a human-editorial
// sign-off gate before publication. Tune the topic list here as guidance evolves.
export const PUBLIC_INTEREST_TOPICS = [
    'election', 'vote', 'referendum', 'ballot',
    'vaccine', 'public health', 'pandemic', 'outbreak',
    'immigration', 'asylum', 'climate policy', 'legislation', 'court ruling',
] as const;

/** True when generated text appears to touch a public-interest matter (AC1.3 trigger). */
export function isPublicInterestText(text: string | null | undefined): boolean {
    return !!text && includesAny(text, PUBLIC_INTEREST_TOPICS);
}

// ── Runtime overrides (layer 2) ─────────────────────────────────────────────────
// Overlay a platform_config override on a static default so an admin can retune the most
// volatile thresholds without a redeploy. Falls back to the static default when unset.

/**
 * Effective EU jurisdiction set: static 27 members UNION any extra codes an admin configured
 * (e.g. EEA additions). Never shrinks below the statutory list.
 */
export async function getEffectiveEuCountries(): Promise<ReadonlySet<string>> {
    const extra = (await getPlatformConfig(CONFIG_KEYS.COMPLIANCE_EU_EXTRA_COUNTRIES)) as string[] | null;
    if (!extra?.length) return EU_MEMBER_STATE_SET;
    return new Set([...EU_MEMBER_STATES, ...extra.map(c => c.trim().toUpperCase())]);
}

/** Effective systemic-risk FLOP threshold (admin-overridable). */
export async function getSystemicRiskFlopThreshold(): Promise<number> {
    const override = (await getPlatformConfig(CONFIG_KEYS.COMPLIANCE_SYSTEMIC_RISK_FLOPS)) as number | null;
    return typeof override === 'number' && override > 0 ? override : SYSTEMIC_RISK_FLOP_THRESHOLD;
}
