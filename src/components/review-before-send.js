/**
 * src/components/review-before-send.js
 *
 * US-LEGAL-1.4: Non-dismissable "Review before sending" warning for every outbound
 * action (email send, social post, calendar invite, etc.).
 *
 * Usage:
 *   window.ReviewBeforeSend.show({ actionType, taskRunId, container })
 *     → Injects the banner into container and returns a Promise<'confirmed'|'cancelled'>
 *
 *   window.ReviewBeforeSend.guard(button, { actionType, taskRunId })
 *     → Wraps a button so every click shows the banner and waits for confirmation
 *       before the original handler fires. Logs warning display to the backend.
 *
 * The banner is NEVER permanently dismissable — it renders on every send action.
 */
(function () {
  'use strict';

  const HIGH_STAKES_TYPES = ['send_email', 'post_to_cms', 'publish_post', 'send_bulk_email_small', 'bulk_email', 'charge_customer'];

  /**
   * Log that the "review before send" warning was displayed.
   * Fire-and-forget; failures are silenced.
   */
  async function _logWarningDisplay(actionType, taskRunId) {
    try {
      await fetch('/.netlify/functions/log-send-warning-displayed', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ actionType, taskRunId, displayedAt: new Date().toISOString() }),
      });
    } catch (_) { /* non-fatal */ }
  }

  /**
   * Inject the banner into `container` and resolve with 'confirmed' or 'cancelled'
   * when the user clicks Confirm or Cancel.
   *
   * @param {{ actionType: string, taskRunId?: number, container: HTMLElement }} opts
   * @returns {Promise<'confirmed'|'cancelled'>}
   */
  function show({ actionType, taskRunId, container }) {
    return new Promise((resolve) => {
      const isHighStakes = HIGH_STAKES_TYPES.includes(actionType);
      const highStakesNote = isHighStakes
        ? `<p class="mt-2 text-xs text-amber-800 font-semibold">
             ⚠ This action may be difficult or impossible to reverse once sent.
             For decisions affecting individuals' legal rights (hiring, credit, medical, legal claims)
             you must have a human review this output before proceeding.
           </p>`
        : '';

      const bannerId = `rbsw-${Date.now()}`;
      const html = `
        <div id="${bannerId}" role="alert" aria-live="assertive"
             class="rounded-lg border border-amber-300 bg-amber-50 px-4 py-3 mb-3 text-sm text-amber-900">
          <div class="flex items-center gap-2 mb-1 font-semibold">
            <span aria-hidden="true">⚠️</span>
            <span>Review before sending</span>
          </div>
          <p>
            This content was generated by an AI and may contain inaccuracies, omissions, or
            fabricated information even where it appears confident. Always verify the output
            before sending or publishing. This is not professional (legal, medical, financial,
            or tax) advice.
          </p>
          ${highStakesNote}
          <div class="mt-3 flex gap-2">
            <button data-rbsw-confirm
                    class="px-3 py-1.5 rounded bg-amber-600 hover:bg-amber-700 text-white text-xs font-semibold">
              I've reviewed — proceed
            </button>
            <button data-rbsw-cancel
                    class="px-3 py-1.5 rounded bg-white border border-amber-400 text-amber-800 text-xs font-semibold hover:bg-amber-100">
              Cancel
            </button>
          </div>
        </div>`;

      // Prepend banner into container
      const wrapper = document.createElement('div');
      wrapper.innerHTML = html;
      const bannerEl = wrapper.firstElementChild;
      container.prepend(bannerEl);

      // Log evidentiary display
      _logWarningDisplay(actionType, taskRunId);

      bannerEl.querySelector('[data-rbsw-confirm]').addEventListener('click', () => {
        bannerEl.remove();
        resolve('confirmed');
      });
      bannerEl.querySelector('[data-rbsw-cancel]').addEventListener('click', () => {
        bannerEl.remove();
        resolve('cancelled');
      });
    });
  }

  /**
   * Wrap a send/publish button so that every click shows the banner first.
   * The original click handler fires only if the user confirms.
   *
   * @param {HTMLElement} button
   * @param {{ actionType: string, taskRunId?: number, container?: HTMLElement }} opts
   */
  function guard(button, { actionType, taskRunId, container }) {
    if (button.dataset.rbswGuarded) return;
    button.dataset.rbswGuarded = '1';

    button.addEventListener('click', async function (e) {
      e.preventDefault();
      e.stopImmediatePropagation();

      const bannerContainer = container || button.closest('form') || button.parentElement;
      const decision = await show({ actionType, taskRunId, container: bannerContainer });

      if (decision === 'confirmed') {
        // Re-dispatch a synthetic click so the original handler fires
        const syntheticClick = new MouseEvent('click', { bubbles: true, cancelable: true });
        syntheticClick.__rbswConfirmed = true;
        button.dispatchEvent(syntheticClick);
      }
      // 'cancelled' — do nothing
    }, true); // capture phase so we intercept before other handlers

    // Allow confirmed synthetic clicks to pass through
    button.addEventListener('click', function (e) {
      if (e.__rbswConfirmed) return; // let it proceed
    }, true);
  }

  window.ReviewBeforeSend = { show, guard };
})();
